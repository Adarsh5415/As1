function displayMessage(fruit) {
    switch (fruit) {
      case "apple":
        console.log("Apples are delicious!");
        break;
      case "banana":
        console.log("Bananas are a great source of potassium!");
        break;
      case "orange":
        console.log("Oranges are packed with vitamin C!");
        break;
      case "mango":
        console.log("Mangoes are the king of fruits!");
        break;
      default:
        console.log("Unknown fruit");
        break;
    }
}

  